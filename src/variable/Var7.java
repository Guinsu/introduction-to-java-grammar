package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; //정수타입 리터럴 = 100
        double b = 10.5; // 실수타입 리터럴 = 10.5
        boolean c = true; // 불리언(boolean) true, false 입력가능 불리언 타입 리터럴 = true
        char d = 'A'; // 문자하나
        String e = "Hello java"; // 문자열, 문자열을 다루기 위한 특별한 타입

        //리터럴은 개발자가 변수에 선언하는 값들을 리터럴이라고 한다.

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
